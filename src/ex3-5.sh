#!/bin/bash

echo "프로그램 시작"

# 'my_ls'라는 이름의 내부 함수 정의
my_ls() {
    echo "함수 안으로 들어왔습니다."
    # eval을 사용하여 "ls [옵션]" 문자열을 실제 명령어로 실행
    # $1은 함수에게 전달된 첫 번째 인자입니다.
    eval "ls $1"
}

# 사용자가 입력한 인자($1)를 함수에게 넘겨줌
# 실행 예: ./ex3-5.sh -l
if [ -z "$1" ]; then
    echo "옵션을 입력하지 않아 기본 ls를 실행합니다."
    my_ls ""
else
    echo "옵션 $1 을 적용하여 실행합니다."
    my_ls "$1"
fi

echo "프로그램 종료"
